export interface Partner {
	id: string;
	name: string;
	type: 'HOSPITAL' | 'PHARMACY' | 'DOCTOR' | 'AMBULANCE' | 'AUTO_REPAIR' | 'LAPTOP_REPAIR' | 'MOBILE_REPAIR';
	category: 'LIFE' | 'NON_LIFE';
	status: 'ACTIVE' | 'PENDING' | 'INACTIVE';
	email: string;
	phone: string;
	address: string;
	location: string;
	tradeLicense?: string;
	tinNumber?: string;
	
	// Discount config
	discountEnabled: boolean;
	discountPercentage: number;
	minDiscount: number;
	maxDiscount: number;
	discountType: string;
	
	// Cashless config
	cashlessEnabled: boolean;
	cashlessLimit: number;
	autoApprovalThreshold: number;
	preAuthRequired: boolean;
	authValidityDays: number;
	requiredDocuments: string[];
	
	// Additional fields
	rating?: number;
	services?: number;
	outlets?: number;
	vehicles?: number;
	specialty?: string;
	brands?: string;
	serviceType?: string;
}

