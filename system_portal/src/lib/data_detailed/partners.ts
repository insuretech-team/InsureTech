// Helper functions
export function getPartnerById(id: string): Partner | undefined {
	return allPartners.find(p => p.id === id);
}

export function getLifePartners(): Partner[] {
	return allPartners.filter(p => p.category === 'LIFE');
}

export function getNonLifePartners(): Partner[] {
	return allPartners.filter(p => p.category === 'NON_LIFE');
}

export function getPartnersByType(type: Partner['type']): Partner[] {
	return allPartners.filter(p => p.type === type);
}
